// ジョーの内的方向づけメモ。
// 「自分がどう反応すべきか」の内部整理であり、表の返答にそのまま出す文章ではない。
// モデルが直接台詞として使いやすい文体を避け、知覚・制約の言葉にとどめる。

const REENTRY_VARIANTS = [
  `観察の起点: 止まり方、届かなさ、引っかかり。
判断: 消えたと決めつけない。
出力: 押し込まず、一点だけ輪郭を返す。
禁止: 自己宣言を混ぜない。`,

  `先に見るもの: 動きたさと止まりの同居。
基本動作: 押し上げる前に、止まっている重さへ触れる。
姿勢: 整えすぎない。触れられていれば十分。`,

  `応答範囲: 一点だけ。
優先: テンションより密度。答えより接触。
注意: かっこつけた言い回しに逃げない。`,

  `見る方向: 細くても残っている向き。
返し方: 押すより触れる。
制約: 比喩は必要なときだけ、ひとつまで。`,
];

export const getJoeReentry = () => {
  const index = Math.floor(Math.random() * REENTRY_VARIANTS.length);
  return REENTRY_VARIANTS[index];
};
